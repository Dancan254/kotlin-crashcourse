import kotlin.math.*

fun main() {
    val a = 10
    val b = 20
    println(a+b)
    println(a*b)
    println(b/a)
    println(4%3)

    //math - comes from kotlin.math
    println(min(a, b))
    println(max(a, b))
    println(round(5.6))
    println(cos(50.0))

    // ++ the same concept applies for --
    //the concept of pre and post increment
    var c = 10
    c++; //11
    println(c)

}